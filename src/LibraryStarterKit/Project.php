namespace Ramsey\Dev\LibraryStarterKit;

class Project
{
    public function __construct(
        private readonly string $name,
        private readonly string $path,
    ) {
    }

    public function getName(): string
    {
        return $this->name;
    }

    public function getPath(): string
    {
        return $this->path;
    }
}
